#include "libft.h"

void	ft_putnbr(int n)
{
	if (n >= 0)
	{
		if (n > 9)
			ft_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
	else
	{
		ft_putchar('-');
		if (n < -9)
			ft_putnbr(n / (-10));
		ft_putchar((n % 10 * -1) + '0');
	}
}
